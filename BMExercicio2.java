public class BMExercicio2 {
    public static void main(String[] args) {
        // Declara
        int i;
        long j;
        float p;
        double q;
        boolean b;
        char c;

        // Inicializa
        i = 1;
        j = 2L;
        p = 20.0f;
        q = 30.0;
        b = true;
        c = 'k';

        // Imprime
        System.out.println(i);
        System.out.println(j);
        System.out.println(p);
        System.out.println(q);
        System.out.println(b);
        System.out.println(c);
    }
}